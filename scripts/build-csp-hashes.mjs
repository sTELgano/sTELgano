// CSP hash computer. Walks every HTML file the v2 build emits
// under public/, extracts every inline <script>…</script> body,
// computes a SHA-256 over the exact bytes, and writes the set of
// unique hashes to src/csp_hashes.ts.
//
// The worker reads this list at build time (esbuild inlines the
// import) and emits them in the `script-src` CSP directive. That
// lets us ship strict CSP without `'unsafe-inline'` — every
// inline script we know about is explicitly allow-listed by
// content hash, and anything the attacker injects won't match a
// pinned hash and will be blocked by the browser.
//
// CSP rule: 'sha256-<base64-of-raw-digest>' — base64 (standard,
// not URL), including padding. The hash is over the exact byte
// content between <script> and </script> — whitespace matters.
// Anything that changes those bytes changes the hash; this script
// runs on every build so drift is a compile-time surfaced error
// (404s at runtime). Must run AFTER build-html.mjs so that the
// assembled output files exist under public/.

import { createHash } from "node:crypto";
import { readdir, readFile, writeFile } from "node:fs/promises";
import { join, sep } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = fileURLToPath(new URL("..", import.meta.url));
const PUBLIC_DIR = join(ROOT, "public");
const OUT_PATH = join(ROOT, "src/csp_hashes.ts");

// Non-greedy match; ignores <script src="…"> (external scripts are
// allowed by `'self'` in CSP separately from inline hashes).
const INLINE_SCRIPT_RE = /<script(?![^>]*\bsrc=)[^>]*>([\s\S]*?)<\/script>/g;

async function walk(dir) {
  const entries = await readdir(dir, { withFileTypes: true }).catch(() => []);
  const files = [];
  for (const entry of entries) {
    const full = join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...(await walk(full)));
    } else if (entry.isFile() && entry.name.endsWith(".html")) {
      files.push(full);
    }
  }
  return files;
}

function sha256Base64(body) {
  return createHash("sha256").update(body, "utf8").digest("base64");
}

async function build() {
  const htmlFiles = await walk(PUBLIC_DIR);
  const hashes = new Set();
  let totalInline = 0;

  for (const file of htmlFiles) {
    const source = await readFile(file, "utf8");
    let m;
    INLINE_SCRIPT_RE.lastIndex = 0;
    // biome-ignore lint/suspicious/noAssignInExpressions: standard regex exec loop
    while ((m = INLINE_SCRIPT_RE.exec(source)) !== null) {
      totalInline++;
      const body = m[1];
      hashes.add(sha256Base64(body));
    }
  }

  const list = Array.from(hashes).sort();
  const rel = (p) =>
    p
      .slice(ROOT.length + 1)
      .split(sep)
      .join("/");

  const ts = `// SPDX-License-Identifier: AGPL-3.0-only
// Generated by scripts/build-csp-hashes.mjs — do not edit by hand.
// Regenerated on every \`npm run build\`. Adding or removing an
// inline <script> in any src/client/pages/**/*.html or
// src/client/templates/**/*.html file means this list changes and
// the worker re-emits a different CSP \`script-src\`.
//
// Scanned: ${htmlFiles.length} HTML files under public/
// Total inline <script> blocks found: ${totalInline}
// Unique SHA-256 hashes:               ${list.length}

export const INLINE_SCRIPT_HASHES: readonly string[] = [
${list.map((h) => `  "sha256-${h}",`).join("\n")}
];
`;

  await writeFile(OUT_PATH, ts, "utf8");
  console.log(
    `build-csp-hashes: scanned ${htmlFiles.length} files, found ${totalInline} inline scripts (${list.length} unique) → ${rel(OUT_PATH)}`,
  );
}

build().catch((err) => {
  console.error("build-csp-hashes failed:", err);
  process.exit(1);
});
